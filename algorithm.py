# задаем начальный массив
arr = ['apple', 'banana', 'cherry', 'dog', 'elephant', 'frog']

# создаем пустой массив для хранения результатов
new_arr = []

# проходим по каждому элементу начального массива
for i in range(len(arr)):
    # проверяем, что длина элемента меньше или равна 3 символам
    if len(arr[i]) <= 3:
        # если да, то добавляем его в новый массив
        new_arr.append(arr[i])

# выводим новый массив на экран
print(new_arr)